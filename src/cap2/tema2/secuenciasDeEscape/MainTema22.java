package cap2.tema2.secuenciasDeEscape;

public class MainTema22 {

	/*
	 *  Secuencias de Escape
	 *  	\b	retroceso
	 *  	\n	Salto de Linea
	 *  	\t	Tabulador horizontal
	 *  	\\	Barra invertida \
	 *  	\'	Comilla simplre
	 *  	\"	Comilla doble
	 */
	
	
	public static void main(String[] args) {
		System.out.println("Java\t\"mañana\"\nVB\t\"tarde\"");
		System.out.println("a\\b");
		System.out.println("I\'m Batman");
		System.out.println("\"Soy honesto\"");
	}

}
